"""Класификация на договор по заглавие в гражданско-значим сектор.

Целта: на картата да остават само ремонти, които реално засягат хората —
пътища/тротоари, паркове и спортни площадки, улично осветление, ВиК,
обществени сгради и площади. Изключваме вътрешни/сградни работи без
обществен достъп (покриви, фасади, енергийна ефективност, офиси, асансьори,
болнични интериори) и чисти доставки/услуги.

`classify(title)` -> код на сектор. `SECTORS` дава човешките имена.
`is_public(title)` -> bool. Логиката е keyword-driven: ако заглавието
съдържа ключова дума за обществена инфраструктура, секторът печели дори при
наличие на „покрив"/„фасада" (напр. „улични настилки след ВиК авария").
"""
import re

SECTORS = {
    "roads": "Пътища и тротоари",
    "water": "ВиК",
    "parks": "Паркове и площадки",
    "lighting": "Улично осветление",
    "public": "Обществени сгради и площади",
    "other": "Друго / неприложимо",
}

# приоритет при няколко съвпадения (по-конкретното/уличното печели)
_ORDER = ["roads", "water", "parks", "lighting", "public"]

_PATTERNS = {
    "roads": re.compile(
        r"тротоар|асфалт|настилк|улиц|улич|пътна|кръстовищ|паркинг|велоале|"
        r"светофар|трафик|пътн[аои]|рехабилитац[^.]{0,25}(улиц|път|пътн)|"
        r"\bпът[^е]|реконструкц[^.]{0,25}(улиц|път)|мост\b", re.I),
    "water": re.compile(
        r"\bвик\b|водопровод|канализац|водоснаб|пречиствател|дъждовн[аи] вод|"
        r"отвеждан[ег] на вод|водопроводн", re.I),
    "parks": re.compile(
        r"парк(?!инг)|зеленин|озеленяв|градин|детск[аио][^.]{0,12}площадк|"
        r"спортн[аио][^.]{0,12}(площадк|игрищ|съоръж|терен)|игрищ|стадион|"
        r"алея|крайбрежн|плаж|зона за отдих", re.I),
    "lighting": re.compile(r"осветлен|освет[ия]", re.I),
    "public": re.compile(
        r"читалищ|библиотек|\bмузе|пазар\b|автогар|спирк|перон|обществен[аио][^.]{0,12}(сграда|тоалетн|простр)|"
        r"площад(?!ка)|градск[аи][^.]{0,12}(център|среда|простр)|гробищн", re.I),
}

# силно изключващи (вътрешни/сградни работи без обществен достъп) — само
# когато НЯМА обществена ключова дума по-горе
_EXCLUDE = re.compile(
    r"покрив|фасад|енергийн[аои][^.]{0,8}ефективн|саниран|топлоизолац|"
    r"асансьор|климатиз|отоплителн|котел|газифик|фотоволта|"
    r"стерилизац|интериор|офис|кабинет|канцелар|вентилац", re.I)


def classify(title: str) -> str:
    t = title or ""
    for s in _ORDER:
        if _PATTERNS[s].search(t):
            return s
    return "other"


def is_public(title: str) -> bool:
    return classify(title) != "other"


if __name__ == "__main__":
    import sys
    sys.stdout.reconfigure(encoding="utf-8")
    tests = [
        "Ремонт на покрив на СУ",
        "Реконструкция и рехабилитация на улична мрежа в община Гоце Делчев",
        "Възстановяване на улични настилки след отстранени ВиК аварии",
        "Ремонт на открити спортни площадки в ОУ",
        "Внедряване на мерки за енергийна ефективност",
        "РЕКОНСТРУКЦИЯ НА СИСТЕМАТА ЗА ВЪНШНО ИЗКУСТВЕНО ОСВЕТЛЕНИЕ",
        "Изграждане на асансьор в сградата",
        "Текущ ремонт на тротоари с бетонова настилка",
    ]
    for t in tests:
        print(f"{classify(t):9} | {t[:60]}")
