def company_detail(eik):
    con = _con(); B = _blend()
    base = con.execute("SELECT MAX(supplier_name) nm, COUNT(*) n, COALESCE(SUM(amount_eur),0) won, "
                       "COUNT(DISTINCT buyer_eik) buyers, SUM(CASE WHEN offers_count<=1 THEN 1 ELSE 0 END) single "
                       "FROM contracts WHERE supplier_eik=?", (eik,)).fetchone()
    if not base or not base["n"]:
        con.close(); return None
    org = con.execute("SELECT name,legal_form FROM organizations WHERE eik=?", (eik,)).fetchone()
    flb = {r["code"]: r["n"] for r in con.execute(
        "SELECT code,COUNT(*) n FROM flags WHERE target_type='contract' AND target_id IN "
        "(SELECT id FROM contracts WHERE supplier_eik=?) GROUP BY code", (eik,))}
    susp = []; maxrisk = 0.0
    for r in con.execute("SELECT id,buyer_name,amount_eur,cpv FROM contracts WHERE supplier_eik=?", (eik,)):
        b = B.get(r["id"], 0); maxrisk = max(maxrisk, b)
        fr = [{"code": x["code"], "evidence": json.loads(x["evidence"])}
              for x in con.execute("SELECT code,evidence FROM flags WHERE target_type='contract' AND target_id=?", (r["id"],))]
        if fr:
            susp.append({"id": r["id"], "buyer": r["buyer_name"],
                         "amount_eur": round(r["amount_eur"]) if r["amount_eur"] else None,
                         "obshtina": _obshtina(r["buyer_name"]), "reasons": _reasons(fr), "risk": round(b, 3)})
    susp.sort(key=lambda x: -x["risk"]); susp = susp[:20]
    owners = [{"person_key": r["person_key"], "name": r["name"], "role": r["role"], "id_type": r["indent_type"]}
              for r in con.execute("SELECT r.person_key,r.role,p.name,p.indent_type FROM roles r "
                                   "JOIN persons p ON r.person_key=p.person_key WHERE r.eik=?", (eik,))]
    by_year = []
    for r in con.execute("SELECT substr(contract_date,1,4) y, COALESCE(SUM(amount_eur),0) s, COUNT(*) n "
                         "FROM contracts WHERE supplier_eik=? AND contract_date IS NOT NULL GROUP BY y ORDER BY y", (eik,)):
        if r["y"]:
            by_year.append({"year": r["y"], "value_eur": round(r["s"]), "contracts": r["n"]})
    con.close()
    return {"eik": eik, "name": (org["name"] if org else None) or base["nm"], "legal_form": org["legal_form"] if org else None,
            "contracts": base["n"], "won_eur": round(base["won"]), "buyers": base["buyers"],
            "single_bid_pct": round(100 * base["single"] / max(1, base["n"])), "risk_pct": round(100 * maxrisk),
            "flags": flb, "owners": owners, "suspicious": susp, "by_year": by_year,
            "income": None, "ml_why": ML_WHY}


def person_detail(key):
    con = _con()
    p = con.execute("SELECT name,indent_type FROM persons WHERE person_key=?", (key,)).fetchone()
    comps = []
    for r in con.execute("SELECT r.eik, r.role FROM roles r WHERE r.person_key=?", (key,)):
        w = con.execute("SELECT MAX(supplier_name) nm, COALESCE(SUM(amount_eur),0) s, COUNT(*) n "
                        "FROM contracts WHERE supplier_eik=?", (r["eik"],)).fetchone()
        org = con.execute("SELECT name FROM organizations WHERE eik=?", (r["eik"],)).fetchone()
        comps.append({"eik": r["eik"], "name": (org["name"] if org else None) or w["nm"] or r["eik"],
                      "role": r["role"], "won_eur": round(w["s"]), "contracts": w["n"]})
    con.close()
    comps.sort(key=lambda x: -x["won_eur"])
    return {"person_key": key, "name": p["name"] if p else None, "id_type": p["indent_type"] if p else None,
            "companies": comps, "total_won_eur": sum(c["won_eur"] for c in comps), "income": None}


def _contract_bundle(con, cid):
    c = con.execute("SELECT * FROM contracts WHERE id=?", (cid,)).fetchone()
    if not c:
        return None
    flags = [{"code": r["code"], "evidence": json.loads(r["evidence"])}
             for r in con.execute("SELECT code,evidence FROM flags WHERE target_type='contract' AND target_id=?", (cid,))]
    sc = con.execute("SELECT score,level FROM integrity_scores WHERE target_type='contract' AND target_id=?", (cid,)).fetchone()
    ml = con.execute("SELECT p_adverse FROM ml_scores WHERE contract_id=?", (cid,)).fetchone()
    det = sc["score"] if sc else 0.0
    p = ml["p_adverse"] if ml else 0.0
    return {"type": "contract", "supplier": c["supplier_name"], "buyer": c["buyer_name"],
            "amount_eur": round(c["amount_eur"]) if c["amount_eur"] else None, "cpv": c["cpv"],
            "offers_count": c["offers_count"], "integrity_level": sc["level"] if sc else None,
            "integrity_score": det, "ml_p_adverse": p, "blended_risk": round(0.7 * det + 0.3 * p, 3), "flags": flags}


def _company_bundle(con, eik):
    a = con.execute("SELECT COUNT(*) n, COALESCE(SUM(amount_eur),0) won, COUNT(DISTINCT buyer_eik) buyers, "
                    "SUM(CASE WHEN offers_count<=1 THEN 1 ELSE 0 END) single, MAX(supplier_name) nm "
                    "FROM contracts WHERE supplier_eik=?", (eik,)).fetchone()
    org = con.execute("SELECT name FROM organizations WHERE eik=?", (eik,)).fetchone()
    fl = {r["code"]: r["n"] for r in con.execute(
        "SELECT code,COUNT(*) n FROM flags WHERE target_type='contract' AND target_id IN "
        "(SELECT id FROM contracts WHERE supplier_eik=?) GROUP BY code", (eik,))}
    return {"type": "company", "eik": eik, "name": (org["name"] if org else None) or a["nm"],
            "contracts_won": a["n"], "won_eur": round(a["won"]), "distinct_buyers": a["buyers"],
            "single_bid_pct": round(100 * a["single"] / max(1, a["n"])), "flags": fl}


def _shared_owner_bundle(con):
    r = con.execute("SELECT evidence FROM flags WHERE code='shared_owner_cross_tenders' ORDER BY severity DESC LIMIT 1").fetchone()
    if not r:
        return None
    e = json.loads(r["evidence"])
    return {"type": "shared_owner_across_winning_firms", "person_name": e["person_name"],
            "id_type": e["id_type"], "winning_companies": e["winning_companies"], "company_count": e["company_count"]}


_NARRATE = """Ти си обясняващ асистент за прозрачност на обществените поръчки. Обясни на прост български
защо посоченото е маркирано като СИГНАЛ ЗА РИСК, използвайки САМО фактите и числата по-долу.
Правила: НЕ изчислявай и НЕ измисляй нови числа — ползвай само дадените. Това е обективен сигнал по
индикатори, НЕ е обвинение и НЕ е присъда. Пиши 3–5 изречения. Завърши с дисклеймър, че това е
автоматичен сигнал за проверка, а не доказателство за нарушение.

ДАННИ (JSON):
{data}

Обяснение:"""


def explain(target):
    """retrieve (deterministic) → narrate (LLM, computes nothing). Returns {bundle, narrative}."""
    import agent
    con = _con()
    if target.startswith("company:"):
        bundle = _company_bundle(con, target.split(":", 1)[1])
    elif target == "shared-owner":
        bundle = _shared_owner_bundle(con)
    else:
        bundle = _contract_bundle(con, target)
    con.close()
    if not bundle:
        return {"error": "no evidence", "bundle": None, "narrative": None}
    narrative = agent.generate(_NARRATE.format(data=json.dumps(bundle, ensure_ascii=False)))
    return {"bundle": bundle, "narrative": narrative}
